	static torch::TensorOptions getOptions()
	{
		if(torch::cuda::is_available()) {
			return torch::TensorOptions().dtype(torch::kFloat64).device(torch::kCUDA);
		}
		else {
			return torch::TensorOptions().dtype(torch::kFloat64).device(torch::kCPU);
		}
	}
	
	/*
		I use these conversion functions because torch::from_blob() doesn't work with CUDA on my machine
	*/
	
	static torch::Tensor getTensorFromMatrix(const std::vector<Vector>& mat) {
		torch::Tensor tensor = torch::zeros({mat.size(), 3}, getOptions());
		for(int i = 0; i < mat.size(); ++i) {
			for(int j = 0; j < 3; ++j) {
				tensor[i][j] = mat[i][j];
			}
		}
		return tensor;
	}
	
	static torch::Tensor getTensorFromVectorOfVectors(const vector<vector<double>>& mat) {
		torch::Tensor tensor = torch::zeros({mat.size(), mat[0].size()}, getOptions());
		for(int i = 0; i < mat.size(); ++i) {
			for(int j = 0; j < mat[0].size(); ++j) {
				tensor[i][j] = mat[i][j];
			}
		}
		return tensor;
	}
	
	static torch::Tensor getTensorFromVector(const vector<double>& vec) {
		torch::Tensor tensor = torch::zeros(vec.size(), getOptions());
		for(int i = 0; i < vec.size(); ++i) {
			tensor[i] = vec[i];	
		}
		return tensor;
	}
